# Bitwise Operators in Python

x = 10
y = 4

# Output: x & y = 0
print('x & y = ', x & y)

# Output: x | y = 14
print('x | y = ', x | y)

# Output: ~x = -11 
print('~x = ', ~x)

# Output: x ^ y = 14
print('x ^ y = ', x ^ y)

# Output: x >> 2 = 2
print('x >> 2 = ', x >> 2)

# Output: x << 2 = 40
print('x << 2 = ', x << 2)